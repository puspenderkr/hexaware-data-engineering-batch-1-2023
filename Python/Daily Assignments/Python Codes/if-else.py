# If Statement
x = 10
if x > 5:
    print("x is greater than 5")

# If-Else Statement
y = 3
if y % 2 == 0:
    print("y is even")
else:
    print("y is odd")

# If-Elif-Else Statement
grade = 75
if grade >= 90:
    print("A")
elif grade >= 80:
    print("B")
elif grade >= 70:
    print("C")
else:
    print("F")
